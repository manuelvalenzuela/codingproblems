        [Fact]
        public void MinimumDistance_IfAreaSizeAndRowsAreNotSame_ShouldThrowsException()
        {
            var area = new[]
            {
                new [] { 1, 0, 0 },
                new [] { 1, 9, 1 }
            };
            Assert.Throws<ArgumentException>(() => MinimumDistanceProblem.MinimumDistance(3, 3, area));
        }


